    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root@2607");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select count(rollNumber) from student");
            if(rs.next()){
                jLabel2.setText(rs.getString(1));
            }
            ResultSet ts = st.executeQuery("select count(rollNumber) from transfer");
            if(ts.next()){
                jLabel4.setText(ts.getString(1));
            }    
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

