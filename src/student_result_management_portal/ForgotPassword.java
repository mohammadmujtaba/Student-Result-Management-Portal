    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        String ID = jTextField1.getText();
        String answer  = jTextField3.getText();
        String newPassword  = jPasswordField1.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root@2607");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from admin where ID ='"+ID+"' and answer ='"+answer+"'");

            if(rs.next()){
                st.executeUpdate("update admin set password = '"+newPassword+"' where ID ='"+ID+"';");
                JOptionPane.showMessageDialog(this, "Password successfully changed !","Success",JOptionPane.INFORMATION_MESSAGE);
                setVisible(false);
                new adminHome().setVisible(true);
            }
            else
                JOptionPane.showMessageDialog(this, "Incorrect answer entered !","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_loginBtnActionPerformed

