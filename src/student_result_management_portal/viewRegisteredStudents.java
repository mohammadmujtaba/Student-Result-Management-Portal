    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sem = (String) jComboBox1.getSelectedItem();
        String courseName = (String) jComboBox2.getSelectedItem();
        jTextField1.setText("");

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root@2607");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where Semester = '"+sem+"' and courseName = '"+courseName+"'");

            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
            ResultSet count = st.executeQuery("Select count(rollNumber) from student");
            if(count.next()){
                jLabel3.setText(count.getString(1));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

