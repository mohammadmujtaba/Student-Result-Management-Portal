    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNumber = jTextField1.getText();

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root@2607");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select Name,courseName ,Semester , Email, Gender from student where rollNumber = '"+rollNumber+"';");

            if(rs.next()){
                jTextField3.setText(rs.getString(1));
                jTextField2.setText(rs.getString(2));
                jTextField4.setText(rs.getString(3));
                jTextField5.setText(rs.getString(4));
                jTextField6.setText(rs.getString(5));
            }
            else
                JOptionPane.showMessageDialog(this, "Roll Number "+rollNumber+" does not exist !","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

