    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        String ID = jTextField1.getText();
        String username = jTextField2.getText();
        String Email = jTextField4.getText();
        String SecurityQ = (String)jComboBox1.getSelectedItem();
        String answer = jTextField3.getText();
        String password = jPasswordField1.getText();
        String confirmPass = jPasswordField2.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        
        
        if(password.equals("")){
            JOptionPane.showMessageDialog(this, "Password field is empty !", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if(!password.equals(confirmPass))
                JOptionPane.showMessageDialog(this, "Password does not match !", "Error", JOptionPane.ERROR_MESSAGE);
        else{
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root@2607");
                Statement st = con.createStatement();
                st.executeUpdate("insert into admin values('"+ID+"','"+username+"','"+gender+"','"+Email+"','"+SecurityQ+"','"+answer+"','"+password+"')");
                JOptionPane.showMessageDialog(this, "Data successfully saved !", "Registration Successfull", JOptionPane.INFORMATION_MESSAGE);
                setVisible(false);
                new adminHome().setVisible(true);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }

    }//GEN-LAST:event_loginBtnActionPerformed

